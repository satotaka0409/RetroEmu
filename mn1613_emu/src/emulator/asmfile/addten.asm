; 1 + 2 + ... + 10 = 55 をループで求め、結果を R0 に残す
; （R1 を 10..1 で回し、毎回 R0 に加算してから DJNZ で減算・分岐）

        LI   R0, 0          ; 合計初期化
        LI   R1, 10         ; ループカウンタ（加算する値）
loop:
        ADD  R0, R1         ; R0 += 現在の値（10, 9, …, 1）
        DJNZ R1, loop       ; R1 を 1 減らし、0 でなければ loop へ
        HALT                ; R0 = 55 で停止
